#!/usr/bin/env node

const crypto = require("node:crypto");
const fs = require("node:fs");
const path = require("node:path");

function walkFiles(root, entries = []) {
  for (const name of fs.readdirSync(root).sort()) {
    const fullPath = path.join(root, name);
    const stat = fs.statSync(fullPath);

    if (stat.isDirectory()) {
      walkFiles(fullPath, entries);
    } else if (stat.isFile()) {
      entries.push(fullPath);
    }
  }

  return entries;
}

function main() {
  const [distDirArg, outputArg, ...fileArgs] = process.argv.slice(2);

  if (distDirArg === undefined || outputArg === undefined) {
    throw new Error("usage: generate-precache-manifest.js <dist-dir> <output>");
  }

  const distDir = path.resolve(distDirArg);
  const output = path.resolve(outputArg);
  const files = (fileArgs.length === 0
    ? walkFiles(distDir)
    : fileArgs.map((file) => path.resolve(file))
  ).filter((file) => file !== output);
  const hash = crypto.createHash("sha256");
  const urls = [];

  for (const file of files) {
    const relativePath = path.relative(distDir, file).replace(/\\/g, "/");

    hash.update(relativePath);
    hash.update("\0");
    hash.update(fs.readFileSync(file));
    hash.update("\0");
    urls.push(relativePath);
  }

  const body = [
    "// Generated by tools/generate-precache-manifest.js.",
    "self.TRACY_PRECACHE = Object.freeze({",
    `  cacheName: "tracy-app-shell-${hash.digest("hex").slice(0, 16)}",`,
    `  urls: Object.freeze(${JSON.stringify(urls, null, 2).replace(/\n/g, "\n  ")}),`,
    "});",
    "",
  ].join("\n");

  fs.mkdirSync(path.dirname(output), { recursive: true });
  fs.writeFileSync(output, body);
}

try {
  main();
} catch (error) {
  console.error(error.stack || error.message || String(error));
  process.exitCode = 1;
}
